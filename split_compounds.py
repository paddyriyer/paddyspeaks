def main():
    with open('lalitha-sahasranama/data.js', 'r', encoding='utf-8') as f:
        content = f.read()

    json_start = content.index('{')
    json_end = content.rindex('}') + 1
    data = json.loads(content[json_start:json_end])

    # Apply splits to names
    split_count = 0
    for name in data['names']:
        num = name['num']
        if num in SPLITS:
            name['name_iast'] = SPLITS[num]
            if '-' in SPLITS[num]:
                split_count += 1

    print(f"Total names with hyphens: {split_count}")

    # Show first 20 for verification
    print("\nFirst 20 names:")
    for name in data['names'][:20]:
        print(f"  {name['num']:4d}. {name['name_iast']}")

    # Write back
    prefix = content[:json_start]
    suffix = content[json_end:]
    json_str = json.dumps(data, ensure_ascii=False, indent=2)
    with open('lalitha-sahasranama/data.js', 'w', encoding='utf-8') as f:
        f.write(prefix + json_str + suffix)

    print("\nData written successfully.")


if __name__ == '__main__':
    main()
